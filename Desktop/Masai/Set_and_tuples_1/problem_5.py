# **Description:** Write a Python program to find the largest number in a given list.

# **Sample Input:** `[4, 65, 32, 2, 104, 78, 10]`

# **Sample Output:** `104`

def largest_number(input):
    max1=max(input)
    print("Largest number from the List:",max1)


input=[4, 65, 32, 2, 104, 78, 10]
print("Sample input: ",input)
largest_number(input)